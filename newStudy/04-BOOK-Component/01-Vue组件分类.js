/*
Vue.js组件主要分为三类
	1、由vue-router产生的每个页面，他本质上也是一个组件(.vue),主要承载当前页面的HTML结构，会包含获取数据、
	   数据整理、数据可视化等常规业务。整个文件相对较大，但一般不会有props选项和自定义事件，因为他作为路由
	   的渲染，不会被复用，因此也不会对外提供接口
	2、不包含业务、独立、具体功能的基础组件，比如事件选择器、模态框等。这类组件作为项目的基础控件，会被大量使用，
	   因此组件的API进行过高强度的抽象，可以通过不同配置实现不同的功能。
	3、业务组件。他是在业务找你哥被多个页面复用，他与独立组件的区别是，业务组件只在当前项目中会用到，不具有通用性，
	   而且会包含一些业务，比如数据请求。而独立组件不含业务，在任何项目中都可以使用，功能单一，比如一个具有数据
	   校验功能的输入框
*/